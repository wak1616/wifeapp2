def populate_db_from_data():
    from tips_db import init_db
    import sqlite3
    
    init_db()
    conn = sqlite3.connect('instance/tips.db')
    c = conn.cursor()
    
    for tip in TIPS:
        c.execute('''
            INSERT OR IGNORE INTO tips (tip, category)
            VALUES (?, ?)
        ''', (tip['tip'], tip['category']))
    
    conn.commit()
    conn.close()

